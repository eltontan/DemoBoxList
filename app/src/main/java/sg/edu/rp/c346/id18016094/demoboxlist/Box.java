package sg.edu.rp.c346.id18016094.demoboxlist;

public class Box {

    private int bluebox;
    private int orangebox;
    private int brownbox;

    public Box(int bluebox, int orangebox, int brownbox) {
        this.bluebox = bluebox;
        this.orangebox = orangebox;
        this.brownbox = brownbox;
    }

    public int getBluebox() {
        return bluebox;
    }

    public void setBluebox(int bluebox) {
        this.bluebox = bluebox;
    }

    public int getOrangebox() {
        return orangebox;
    }

    public void setOrangebox(int orangebox) {
        this.orangebox = orangebox;
    }

    public int getBrownbox() {
        return brownbox;
    }

    public void setBrownbox(int brownbox) {
        this.brownbox = brownbox;
    }
}
